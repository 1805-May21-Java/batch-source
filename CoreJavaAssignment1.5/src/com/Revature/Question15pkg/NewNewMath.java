package com.Revature.Question15pkg;

public class NewNewMath implements NewMath {

	@Override
	public int add(int n1, int n2) {
		// Addition
		return n1 + n2;
	}

	@Override
	public int subtract(int n1, int n2) {
		//Subtraction
		return n1 - n2;
	}

	@Override
	public int multiply(int n1, int n2) {
		//Multiplication
		return n1 * n2;
	}

	@Override
	public int divide(int n1, int n2) {
		//Division
		return n1 / n2;
	}

}
