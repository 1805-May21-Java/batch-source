package com.revature.assignoop;

public class Order extends Classes{
    private boolean eatsMeat;
    private boolean hasThumbs;
    private boolean isFish;

    public boolean isEatsMeat() {
        return eatsMeat;
    }

    public void setEatsMeat(boolean eatsMeat) {
        this.eatsMeat = eatsMeat;
    }

    public boolean isHasThumbs() {
        return hasThumbs;
    }

    public void setHasThumbs(boolean hasThumbs) {
        this.hasThumbs = hasThumbs;
    }

    public boolean isFish() {
        return isFish;
    }

    public void setFish(boolean fish) {
        isFish = fish;
    }
}
