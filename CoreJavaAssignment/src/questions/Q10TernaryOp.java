package questions;

//Q10TernaryOp contains method findMin(int a, int b) which returns the
//minimum between a and b.
public class Q10TernaryOp {
	
	//findMin returns the result using a ternary operator
	//if a < b, a is returned, else b.
	public int findMin(int a, int b) {
		return (a < b) ? a : b;
	}
}
