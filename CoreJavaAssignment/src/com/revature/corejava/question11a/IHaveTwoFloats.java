package com.revature.corejava.question11a;

// This class houses the two float variables
public class IHaveTwoFloats {

	// Initialize two final float variables to 1f and 2f
	private final float one=1f;
	private final float two=2f;
	
	// Constructor that calls super
	public IHaveTwoFloats() {
		super();
	}

	// Getter to return the value of the one variable
	public float getOne() {
		return one;
	}

	// Getter to return the value of the two variable
	public float getTwo() {
		return two;
	}
	
	
	
	
}
